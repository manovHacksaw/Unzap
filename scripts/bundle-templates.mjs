/**
 * Reads all files under templates/nextjs/ and generates
 * lib/codegen/templateContents.ts with their contents as string literals.
 *
 * Run before `next build` so the API route never needs fs at runtime.
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const templateDir = path.join(__dirname, "..", "templates");
const outputFile = path.join(__dirname, "..", "lib", "codegen", "templateContents.ts");

function readDirRecursive(dir, base = "") {
  const entries = {};
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const relPath = base ? `${base}/${entry.name}` : entry.name;
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      Object.assign(entries, readDirRecursive(fullPath, relPath));
    } else {
      entries[relPath] = fs.readFileSync(fullPath, "utf8");
    }
  }
  return entries;
}

const templates = readDirRecursive(templateDir);
const lines = [
  "// AUTO-GENERATED by scripts/bundle-templates.mjs — do not edit directly.",
  "export const TEMPLATES: Record<string, string> = {",
];

for (const [key, content] of Object.entries(templates)) {
  lines.push(`  ${JSON.stringify(key)}: ${JSON.stringify(content)},`);
}
lines.push("};");

fs.writeFileSync(outputFile, lines.join("\n") + "\n");
console.log(
  `[bundle-templates] Embedded ${Object.keys(templates).length} template files → ${outputFile}`
);
